//              0123456789...
const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';

const shiftChar = (c, shift) => {
    // a helper function to shift one character inside the 
    // alphabet based on the shift value and return the result
    let position = alphabet.indexOf(c);
    // když idexOf vrati -1, znamena to ze znak 'c' neni v abecede 'alphabet'
    if (position == -1) {
        return c;
    }
    let newPosition = (position + shift) % alphabet.length;
    return alphabet.charAt(newPosition);
};
const shiftString = (str, shift) => {
    // a helper function to shift one entire string inside the 
    // alphabet based on the shift value and return the result
    let shiftedStr = "";
    for (let c of str) {
        shiftedStr = shiftedStr + shiftChar(c, shift);
    }
    return shiftedStr;
};
const caesarDecipher = (cipherText, usedKey) => {
    // your implementation goes here
    // good to know: 
    //    str.indexOf(c) - returns the index of the specified character in the string
    //    str.charAt(i) - returns the character at the specified index in the string
    //    when the shifted character is out of bound, it goes back to the beginning and count on from there
    const decipheredText = shiftString(cipherText, alphabet.length - usedKey);

    return decipheredText;
};

